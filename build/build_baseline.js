#!/usr/bin/env node
/*
 * build_baseline.js — compile the AI-lyrics corpus + human anchors into a
 * nearest-centroid classifier baked to src/baseline.{json,js}.
 *
 *   AI corpus  : every file in corpus/models/*.{js,json}  (Claude, Qwen, + any
 *                pasted-in Grok/Gemini/ChatGPT sets)
 *   Human class: examples/human.js
 *
 * Features are standardized (z-scored) over the whole pool, then we store the
 * AI centroid and the human centroid. At runtime a song is classified by which
 * centroid it sits closer to — "more like the AI baseline = more AI".
 */
const fs = require("fs");
const path = require("path");
const { extract, FEATURE_NAMES } = require("../src/features.js");

const MODELS_DIR = path.join(__dirname, "..", "corpus", "models");
const OUT_JSON = path.join(__dirname, "..", "src", "baseline.json");
const OUT_JS = path.join(__dirname, "..", "src", "baseline.js");

// ---- load AI corpus --------------------------------------------------------
const aiSongs = [];
const perModel = {};
if (fs.existsSync(MODELS_DIR)) {
  for (const file of fs.readdirSync(MODELS_DIR)) {
    if (!/\.(js|json)$/.test(file)) continue;
    const mod = require(path.join(MODELS_DIR, file));
    const songs = (mod.songs || [])
      .map((s) => ({ ...s, lyrics: s.lyrics_en || s.lyrics })) // compare in English
      .filter((s) => (s.lyrics || "").trim().length > 30);
    for (const s of songs) {
      aiSongs.push(s);
      const m = s.model || mod.model || file;
      perModel[m] = (perModel[m] || 0) + 1;
    }
  }
}

// ---- load human anchors ----------------------------------------------------
const humanRaw = require("../examples/human.js");
const humanSongs = humanRaw.filter((h) => (h.text || "").trim().length > 30);

if (!aiSongs.length) {
  console.error("No AI songs found in corpus/models/. Generate them first.");
  process.exit(1);
}

// ---- featurize -------------------------------------------------------------
const aiVecs = aiSongs.map((s) => extract(s.lyrics).values);
const humanVecs = humanSongs.map((h) => extract(h.text).values);
const all = aiVecs.concat(humanVecs);
const D = FEATURE_NAMES.length;

// ---- standardize over the whole pool --------------------------------------
const mean = Array(D).fill(0),
  std = Array(D).fill(0);
for (const v of all) for (let i = 0; i < D; i++) mean[i] += v[i];
for (let i = 0; i < D; i++) mean[i] /= all.length;
for (const v of all) for (let i = 0; i < D; i++) std[i] += (v[i] - mean[i]) ** 2;
for (let i = 0; i < D; i++) std[i] = Math.sqrt(std[i] / all.length) || 1;

const z = (v) => v.map((x, i) => (x - mean[i]) / std[i]);
const centroid = (vecs) => {
  const c = Array(D).fill(0);
  for (const v of vecs) {
    const zv = z(v);
    for (let i = 0; i < D; i++) c[i] += zv[i];
  }
  return c.map((x) => x / vecs.length);
};

const aiCentroid = centroid(aiVecs);
const humanCentroid = centroid(humanVecs);

const baseline = {
  builtAt: process.env.BUILD_STAMP || "unstamped",
  featureNames: FEATURE_NAMES,
  scaler: { mean, std },
  centroids: { ai: aiCentroid, human: humanCentroid },
  temperature: 1.0,
  meta: { aiCount: aiSongs.length, humanCount: humanSongs.length, perModel },
};

fs.writeFileSync(OUT_JSON, JSON.stringify(baseline, null, 2));
fs.writeFileSync(
  OUT_JS,
  "/* AUTO-GENERATED by build/build_baseline.js — do not edit */\n" +
    "globalThis.SLOP_BASELINE = " +
    JSON.stringify(baseline) +
    ";\n"
);

// ---- diagnostics -----------------------------------------------------------
const dist = (a, b) => Math.sqrt(a.reduce((s, x, i) => s + (x - b[i]) ** 2, 0));
console.log(`\n=== baseline built ===`);
console.log(`AI songs: ${aiSongs.length}  | human anchors: ${humanSongs.length}`);
console.log(`per model:`, perModel);
console.log(`\nfeature           AI mean    human mean   (raw)`);
for (let i = 0; i < D; i++) {
  const aiM = aiVecs.reduce((s, v) => s + v[i], 0) / aiVecs.length;
  const huM = humanVecs.reduce((s, v) => s + v[i], 0) / humanVecs.length;
  console.log(
    `  ${FEATURE_NAMES[i].padEnd(16)} ${aiM.toFixed(3).padStart(8)}   ${huM
      .toFixed(3)
      .padStart(8)}`
  );
}

// leave-one-out separation on the labelled pool
const { classify } = require("../src/features.js");
let correct = 0,
  total = 0;
const probe = (text, label) => {
  const r = classify(text, baseline);
  total++;
  if ((r.pAI >= 50 ? "ai" : "human") === label) correct++;
  return r.pAI;
};
const aiP = aiSongs.map((s) => probe(s.lyrics, "ai"));
const huP = humanSongs.map((h) => probe(h.text, "human"));
const avg = (a) => (a.reduce((x, y) => x + y, 0) / a.length).toFixed(1);
console.log(`\nresubstitution P(AI):  AI avg ${avg(aiP)}%   human avg ${avg(huP)}%`);
console.log(`nearest-centroid accuracy: ${correct}/${total}\n`);
console.log(`wrote ${path.relative(process.cwd(), OUT_JSON)} and baseline.js`);
