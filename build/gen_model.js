// Regenerate src/ext/model.js: the trained combined_model.json + aiDenseMean/aiDenseStd
// (per-dense-feature stats over the whole AI corpus, for the joker z-scores). Uses the EXACT
// node denseDict so feature computation matches the extension (which mirrors it).
const fs = require('fs'), path = require('path'), ROOT = path.join(__dirname, '..');
const feats = require(path.join(ROOT, 'src/features.js'));
const pat = require(path.join(ROOT, 'analysis/patterns.js'));
const t3 = require(path.join(ROOT, 'analysis/tier3_detectors.js'));

function denseDict(text) {
  const d = {};
  try { const f = feats.extract(text); f.names.forEach((k, i) => d['f_' + k] = f.values[i]); } catch (e) {}
  const a = pat.analyze(text); const nL = Math.max(1, a.__nLines); const RATE = new Set(['contentDensity']);
  let cl = 0, rh = 0;
  for (const [k, v] of Object.entries(a)) {
    if (k.startsWith('struct::')) { const n = k.slice(8); d['s_' + n] = RATE.has(n) ? v : v / nL; }
    else if (k.startsWith('cliche::')) cl += v; else if (k.startsWith('rhyme::')) rh += v;
  }
  d['lex_cliche'] = cl / nL; d['lex_rhyme'] = rh / nL;
  const tf = t3.analyze(text); for (const k in tf) d[k] = tf[k];
  return d;
}

const model = JSON.parse(fs.readFileSync(path.join(ROOT, 'corpus/combined_model.json')));
const DN = model.denseNames;
// gather AI dense vectors
const D = path.join(ROOT, 'corpus/models');
const vecs = [];
for (const f of fs.readdirSync(D).filter(x => x.endsWith('.json')))
  for (const s of (JSON.parse(fs.readFileSync(path.join(D, f))).songs || []))
    if (s.lyrics) { const dd = denseDict(s.lyrics); vecs.push(DN.map(k => +dd[k] || 0)); }
const n = vecs.length;
const mean = DN.map((_, j) => vecs.reduce((a, v) => a + v[j], 0) / n);
const std = DN.map((_, j) => Math.sqrt(vecs.reduce((a, v) => a + (v[j] - mean[j]) ** 2, 0) / n) || 1);

const out = { ...model, aiDenseMean: mean, aiDenseStd: std };
const banner = '/* AUTO-GENERATED by build/gen_model.js — globalThis.SLOP_MODEL.\n' +
  ' * combined_model.json weights + aiDenseMean/aiDenseStd over ' + n + ' AI songs (for joker z-scores).\n */\n';
fs.writeFileSync(path.join(ROOT, 'src/ext/model.js'), banner + 'globalThis.SLOP_MODEL = ' + JSON.stringify(out) + ';\n');
console.log('wrote src/ext/model.js | denseNames:', DN.length, '| AI songs for means:', n, '| vocab:', model.vocab.length);
