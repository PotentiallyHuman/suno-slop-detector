#!/usr/bin/env node
/* build_perspectives.js — bundle the tier-4 perspective modules into ONE browser file.
 * Each module is a self-contained IIFE that attaches to globalThis and guards its Node require()/
 * module.exports behind `typeof require/module`, so plain concatenation runs correctly in a content
 * script (require/module are undefined there → it uses the globals set by earlier files in the bundle
 * and by slop-core/common_words/tier3 loaded earlier in the manifest).
 * Output: src/ext/perspectives.browser.js   (load AFTER tier3.browser.js, BEFORE v2-engine.js)
 */
const fs = require('fs'), path = require('path');
const ROOT = path.join(__dirname, '..');
const ORDER = [
  'analysis/eye_rhymes.js',
  'analysis/prosody.js',
  'analysis/perspectives/rapper.js',
  'analysis/perspectives/poet.js',
  'analysis/perspectives/wit.js',
  'analysis/perspectives/psychologist.js',
  'analysis/perspectives/philosopher.js',
  'analysis/perspectives/storyteller.js',
  'analysis/perspectives.js',
];
let out = '/* AUTO-BUILT bundle — tier-4 craft-perspective lenses. Source: analysis/{eye_rhymes,prosody,perspectives/*,perspectives}.js\n'
        + '   Rebuild: node build/build_perspectives.js   (do NOT edit this file by hand) */\n';
for (const f of ORDER) {
  out += `\n/* ===== ${f} ===== */\n` + fs.readFileSync(path.join(ROOT, f), 'utf8') + '\n';
}
const dest = path.join(ROOT, 'src/ext/perspectives.browser.js');
fs.writeFileSync(dest, out);
console.log('built', path.relative(ROOT, dest), '(' + (out.length/1024).toFixed(1) + ' KB from ' + ORDER.length + ' modules)');
